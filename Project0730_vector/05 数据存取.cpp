#include<iostream>
using namespace std;
#include<vector>

void test01()
{
	vector<int> v1;
	for (int i = 0; i < 10; i++)
	{
		v1.push_back(i);
	}
	//1、利用[]访问
	for (int i = 0; i < v1.size(); i++)
	{
		cout << v1[i] << " ";
	}
	cout << endl;
	//2、利用at访问
	for (int i = 0; i < v1.size(); i++)
	{
		cout << v1.at(i) << " ";
	}
	cout << endl;
	//3、获取第一个元素
	cout << "第一个元素：" << v1.front() << endl;
	//4、获取最后一个元素
	cout << "最后一个元素：" << v1.back() << endl;


}


int main()
{
	test01();
	system("pause");
	return 0;
}